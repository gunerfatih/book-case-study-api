const bookController = require('../../controllers/book.controller');

const router = express.Router();

router
  .route('/')
  .post(bookController.createBook)
  .get(bookController.getBooks);

router
  .route('/:bookId')
  .get(bookController.getBook)
  .patch(bookController.updateBook)
